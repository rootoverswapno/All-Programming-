#include<bits/stdc++.h>
using namespace std;
int main()
{
    int a=10;
    //left shift operator 
       int b=a<<4;
       cout<<b<<endl;
       //a*2^4=160
       //formula of left shift
       //a*b^c
       

       //right shift operator
       int m=a>>4;
       cout<<m<<endl;
       //formula of right shift
       //   a/b^c;

       //~ operator works in system
       
       int v=5;
       int vv=~v;
       cout<<vv<<endl;



    return 0;
}